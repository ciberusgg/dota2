function CJungleSpirits:FireAnnouncerMonologue()
	if self.flAnnouncerCooldown ~= nil and GameRules:GetGameTime() < self.flAnnouncerCooldown then
		return
	end

	local Monologues = g_AnnouncerMonologue
	if Monologues ~= nil and TableLength( Monologues ) > 0 then
		local szMonologue = Monologues[ RandomInt( 1, TableLength( Monologues ) ) ]
		EmitAnnouncerSound( szMonologue )

		self.flAnnouncerCooldown = GameRules:GetGameTime() + ANNOUNCER_COOLDOWN
		self.flNextMonologueTime = GameRules:GetGameTime() + ANNOUNCER_MONOLOGUE_COOLDOWN
	end
end

--------------------------------------------------------------------------------

function CJungleSpirits:FireAnnouncerPregame()
	if self.flAnnouncerCooldown ~= nil and GameRules:GetGameTime() < self.flAnnouncerCooldown then
		return
	end

	if self.bAnnouncerPreGamePlayed ~= nil then
		return
	end

	local GameStart = g_AnnouncerPregame
	if GameStart ~= nil and TableLength( GameStart ) > 0 then
		local szGameStart = GameStart[ RandomInt( 1, TableLength( GameStart ) ) ]
		EmitAnnouncerSound( szGameStart )

		self.flAnnouncerCooldown = GameRules:GetGameTime() + ANNOUNCER_COOLDOWN

		self.bAnnouncerPreGamePlayed = true
	end
end

