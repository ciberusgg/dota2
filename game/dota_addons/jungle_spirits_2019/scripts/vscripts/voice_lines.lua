function CJungleSpirits:FireAnnouncerCarePackageNotification()
	if self.flAnnouncerCooldown ~= nil and GameRules:GetGameTime() < self.flAnnouncerCooldown then
		return
	end

	local VoiceLines = g_AnnouncerCarePackageNotification
	if VoiceLines ~= nil and TableLength( VoiceLines ) > 0 then
		local nLineIndex = RandomInt( 1, TableLength( VoiceLines ) )
		local szVoiceLine = VoiceLines[ nLineIndex ]
		EmitAnnouncerSound( szVoiceLine )

		for i = 1, TableLength( g_AnnouncerOneTimeUseLines ) do
			-- Is this one of the lines we never want to hear more than once per match?
			if szVoiceLine == g_AnnouncerOneTimeUseLines[ i ] then
				VoiceLines[ nLineIndex ] = nil
			end
		end

		self.flAnnouncerCooldown = GameRules:GetGameTime() + ANNOUNCER_COOLDOWN
	end
end

